# === Удаляем второй слайд-пример (Колонтитул / Текст) ===
# python-pptx не имеет прямого метода удаления — используем XML
xml_slides = p.slides._sldIdLst
slides_to_remove = list(xml_slides)[1:]  # все кроме первого
for sld in slides_to_remove:
    rId = sld.attrib['{http://schemas.openxmlformats.org/officeDocument/2006/relationships}id']
    p.part.drop_rel(rId)
    xml_slides.remove(sld)

# === Добавляем новые контентные слайды на layout 5 "Только заголовок" ===
