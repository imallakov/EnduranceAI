def set_placeholder_text(placeholder, text):
    """Записывает строку в placeholder, сохраняя стили первого run'а."""
    tf = placeholder.text_frame
    tf.text = text  # это перезапишет, оставив один параграф

def set_subtitle_multiline(placeholder, text):
    """Многострочный subtitle — отдельные параграфы."""
    tf = placeholder.text_frame
    lines = text.split('\n')
    tf.text = lines[0] if lines else ''
    for line in lines[1:]:
        p = tf.add_paragraph()
        p.text = line

def set_bullets(placeholder, items):
    """Заполняет placeholder списком буллетов из tuples (text, level)."""
    tf = placeholder.text_frame
    tf.clear()
    # tf.clear() оставляет один пустой параграф
    first = True
    for text, level in items:
        if first:
            para = tf.paragraphs[0]
            first = False
        else:
            para = tf.add_paragraph()
        para.text = text
        para.level = level
        # Размер шрифта — побольше для читаемости
        for run in para.runs:
            run.font.size = Pt(18 if level == 0 else 16)

