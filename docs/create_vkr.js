function blank() {
  return new Paragraph({
    spacing: { line: LINE_15, lineRule: LineRuleType.AUTO },
    children: [new TextRun({ text: '', font: FONT, size: SIZE_BODY })],
  });
}

function formula(text, num) {
  return new Paragraph({
    spacing: { line: LINE_15, lineRule: LineRuleType.AUTO, before: 240, after: 240 },
    tabStops: [
      { type: TabStopType.CENTER, position: Math.floor(CONTENT_W / 2) },
      { type: TabStopType.RIGHT, position: CONTENT_W },
    ],
    children: [
      new TextRun({ text: '\t' + text, font: FONT, size: SIZE_BODY, language: LANG_RU }),
      new TextRun({ text: '\t(' + num + ')', font: FONT, size: SIZE_BODY }),
    ],
  });
}

function formulaWhere(text) {
  return new Paragraph({
    alignment: AlignmentType.JUSTIFIED,
    spacing: { line: LINE_15, lineRule: LineRuleType.AUTO },
    children: [new TextRun({ text, font: FONT, size: SIZE_BODY, language: LANG_RU })],
  });
}

function tableCaption(num, title) {
  return new Paragraph({
    alignment: AlignmentType.LEFT,
    spacing: { line: LINE_15, lineRule: LineRuleType.AUTO, before: 240, after: 120 },
    children: [new TextRun({ text: `Таблица ${num} – ${title}`, font: FONT, size: SIZE_BODY, language: LANG_RU })],
  });
}

// Подпись рисунка: "Рисунок 1.1 – Название"
