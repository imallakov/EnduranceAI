const tBorder = { style: BorderStyle.SINGLE, size: 4, color: '000000' };
const tBorders = { top: tBorder, bottom: tBorder, left: tBorder, right: tBorder };

function tCell(text, width, opts = {}) {
  const align = opts.align || AlignmentType.LEFT;
  const fontSize = opts.small ? SIZE_SMALL : SIZE_BODY;
  return new TableCell({
    borders: tBorders,
    width: { size: width, type: WidthType.DXA },
    margins: { top: 60, bottom: 60, left: 80, right: 80 },
    children: [new Paragraph({
      alignment: align,
      spacing: { line: LINE_SINGLE, lineRule: LineRuleType.AUTO },
      children: [new TextRun({ text, font: FONT, size: fontSize, language: LANG_RU })],
    })],
  });
}

