function codeLine(text) {
  return new Paragraph({
    alignment: AlignmentType.LEFT,
    spacing: { line: LINE_SINGLE, lineRule: LineRuleType.AUTO },
    children: [new TextRun({
      text: text || ' ',
      font: 'Courier New',
      size: 22, // 11pt
      language: LANG_RU,
    })],
  });
}

function codeBlock(lines) {
  return lines.map(l => codeLine(l));
}

